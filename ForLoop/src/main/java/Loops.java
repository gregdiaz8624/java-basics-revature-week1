
public class Loops {

	public static void main(String[] args) {
		for(int i=1; i<=10; i++) {
		
			System.out.println(i);
			
		}
	}
	
}

//..One important note is that we used the variable,
//i (which we defined in our declaration statement) 
//inside of our loop. Because the increment statement
//increases the value by one each time, we are effectively
//counting from 1 to 10 and storing that value in the variable 
//i during each iteration of the loop.